# ── helpers ──────────────────────────────────────────────────────────────────

`%||%` <- function(a, b) if (!is.null(a) && length(a) > 0 && a != "") a else b

is_alchemy <- function(year) {
  yr <- suppressWarnings(as.integer(year))
  !is.na(yr) && yr >= 2022
}

to_rname <- function(x) {
  x <- trimws(x)
  x <- gsub("[^A-Za-z0-9 ]", "", x)
  x <- gsub("\\s+(.)", "\\U\\1", x, perl = TRUE)
  x <- gsub("\\s", "", x)
  if (nchar(x) == 0) return("item")
  if (grepl("^[0-9]", x)) x <- paste0("x", x)
  x
}

r_str      <- function(x) paste0('"', gsub('"', '\\"', x), '"')
r_char_vec <- function(v) {
  v <- v[nchar(trimws(v)) > 0]
  if (length(v) == 0) return(NULL)
  if (length(v) == 1) return(r_str(v))
  paste0("c(", paste(sapply(v, r_str), collapse = ",\n           "), ")")
}
r_num_vec  <- function(v) {
  v <- v[nchar(trimws(v)) > 0]
  if (length(v) == 0) return(NULL)
  if (length(v) == 1) return(trimws(v))
  paste0("c(", paste(trimws(v), collapse = ", "), ")")
}

# Parse bulk paste text → list of list(label, value, rname)
parse_bulk <- function(txt) {
  lines <- strsplit(txt, "\n")[[1]]
  lines <- trimws(lines)
  lines <- lines[nchar(lines) > 0]
  lapply(lines, function(ln) {
    parts <- strsplit(ln, "\\|")[[1]]
    parts <- trimws(parts)
    list(
      label = parts[1] %||% "",
      value = if (length(parts) >= 2) parts[2] else "",
      rname = if (length(parts) >= 3) parts[3] else ""
    )
  })
}

build_man_call <- function(d) {
  lines <- c(
    paste0('man(qname         = ', r_str(d$qname), ','),
    paste0('    year          = ', d$year, ',')
  )
  if (nchar(trimws(d$dname %||% "")) > 0)
    lines <- c(lines, paste0('    dname         = ', r_str(d$dname), ','))
  lines <- c(lines,
    paste0('    questionText  = ', r_str(d$questionText), ','),
    paste0('    questionType  = ', r_str(d$questionType), ',')
  )

  qt <- d$questionType

  if (qt %in% c("selectOne", "selectMultiple")) {
    resp_labels <- d$resp_labels
    resp_values <- d$resp_values
    wi_labels   <- d$wi_labels
    wi_types    <- d$wi_types
    rnames      <- d$rnames

    if (!is.null(resp_labels) && length(resp_labels) > 0) {
      lines <- c(lines, paste0('    responses     = ', r_char_vec(resp_labels), ','))
      if (!is.null(rnames) && length(rnames) > 0)
        lines <- c(lines, paste0('    rname         = ', r_char_vec(rnames), ','))
      if (is_alchemy(d$year) && !is.null(resp_values) && length(resp_values) > 0 && any(nchar(trimws(resp_values)) > 0))
        lines <- c(lines, paste0('    reportingValues = ', r_num_vec(resp_values), ','))
    }
    if (!is.null(wi_labels) && length(wi_labels) > 0) {
      lines <- c(lines, paste0('    writeInResponses = ', r_char_vec(wi_labels), ','))
      wt <- wi_types[nchar(trimws(wi_types)) > 0]
      if (length(wt) > 0) {
        if (length(wt) == 1)
          lines <- c(lines, paste0('    writeInTypes  = ', r_str(wt), ','))
        else
          lines <- c(lines, paste0('    writeInTypes  = ', r_char_vec(wt), ','))
      }
      if (nchar(trimws(d$wname %||% "")) > 0)
        lines <- c(lines, paste0('    wname         = ', r_str(d$wname), ','))
    }

  } else if (qt == "selectScale") {
    row_labels <- d$scale_row_labels
    scale_opts <- d$scale_opts
    rnames     <- d$rnames
    scale_vals <- d$scale_vals

    if (!is.null(row_labels) && length(row_labels) > 0) {
      lines <- c(lines, paste0('    responses     = ', r_char_vec(row_labels), ','))
      if (!is.null(rnames) && length(rnames) > 0)
        lines <- c(lines, paste0('    rname         = ', r_char_vec(rnames), ','))
    }
    if (!is.null(scale_opts) && length(scale_opts) > 0) {
      lines <- c(lines, paste0('    scaleOptions  = ', r_char_vec(scale_opts), ','))
      if (is_alchemy(d$year) && !is.null(scale_vals) && length(scale_vals) > 0 && any(nchar(trimws(scale_vals)) > 0))
        lines <- c(lines, paste0('    reportingValues = ', r_num_vec(scale_vals), ','))
    }

  } else if (qt %in% c("textBoxNumeric", "textBoxCharacter")) {
    if (nchar(trimws(d$text_pattern %||% "")) > 0)
      lines <- c(lines, paste0('    responses     = ', r_str(d$text_pattern), ','))
  }

  if (nchar(trimws(d$notes_qname %||% "")) > 0)
    lines <- c(lines, paste0('    notes.qname   = ', r_str(d$notes_qname), ','))
  if (nchar(trimws(d$notes_year %||% "")) > 0)
    lines <- c(lines, paste0('    notes.year    = ', r_str(d$notes_year), ','))

  last <- length(lines)
  lines[last] <- sub(",$", "", lines[last])
  lines <- c(lines, ")")
  paste(lines, collapse = "\n")
}

generate_r_file <- function(records) {
  header <- paste0(
    "# BRC Census man() records\n",
    "# Generated by burnr_form Shiny app on ", Sys.Date(), "\n",
    "library(burnr)\n\n"
  )
  calls <- sapply(records, build_man_call)
  paste0(header, paste(calls, collapse = "\n\n"))
}

