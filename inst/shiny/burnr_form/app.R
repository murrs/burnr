# ── Server ───────────────────────────────────────────────────────────────────

server <- function(input, output, session) {

  rv <- reactiveValues(
    records             = list(),
    resp_row_ids        = character(0),
    scale_row_ids       = character(0),
    writein_row_ids     = character(0),
    editing_idx         = NULL,
    suppress_type_change = FALSE
  )

  # ── Platform badge ─────────────────────────────────────────────────────────
  output$platform_badge <- renderUI({
    req(input$year)
    yr <- suppressWarnings(as.integer(input$year))
    if (is.na(yr)) return(NULL)
    if (yr >= 2022)
      tags$span(class = "platform-badge badge-alchemy",    "Alchemy (2022+)")
    else if (yr >= 2012)
      tags$span(class = "platform-badge badge-surveygizmo","SurveyGizmo era")
    else
      tags$span(class = "platform-badge badge-unknown",    as.character(yr))
  })

  # ── Column headers (vary by type & year) ───────────────────────────────────
  output$response_col_headers <- renderUI({
    qt      <- input$questionType %||% "selectOne"
    alchemy <- is_alchemy(input$year %||% 0)
    if (qt == "selectScale") {
      fluidRow(
        column(7, div(class = "col-header", "Item label (row)")),
        column(4, div(class = "col-header", HTML("R name <span class='param-badge'>rname</span>")))
      )
    } else if (alchemy) {
      fluidRow(
        column(5, div(class = "col-header", "Response label")),
        column(3, div(class = "col-header", HTML("R name <span class='param-badge'>rname</span>"))),
        column(2, div(class = "col-header", HTML("Value <span class='param-badge'>reportingValues</span>"))),
        column(1)
      )
    } else {
      fluidRow(
        column(6, div(class = "col-header", "Response label")),
        column(4, div(class = "col-header", HTML("R name <span class='param-badge'>rname</span>"))),
        column(1)
      )
    }
  })

  output$scale_col_headers <- renderUI({
    alchemy <- is_alchemy(input$year %||% 0)
    if (alchemy) {
      fluidRow(
        column(6, div(class = "col-header", "Scale label")),
        column(3, div(class = "col-header", HTML("Value <span class='param-badge'>reportingValues</span>"))),
        column(1)
      )
    } else {
      fluidRow(
        column(9, div(class = "col-header", "Scale label")),
        column(1)
      )
    }
  })

  # ── Row factory ────────────────────────────────────────────────────────────
  next_id <- local({ n <- 0L; function() { n <<- n + 1L; as.character(n) } })

  make_response_row <- function(rid, label = "", rname = "", value = "") {
    qt      <- isolate(input$questionType) %||% "selectOne"
    alchemy <- is_alchemy(isolate(input$year) %||% 0)
    del_btn <- tags$button(class = "btn btn-xs btn-danger",
      onclick = sprintf("Shiny.setInputValue('delete_resp_row','%s',{priority:'event'})", rid), "×")
    if (qt == "selectScale") {
      div(class = "resp-row", id = paste0("resp_row_", rid),
        fluidRow(
          column(7, textInput(paste0("resp_label_", rid), NULL, value = label, placeholder = "Item being rated")),
          column(4, textInput(paste0("resp_rname_", rid), NULL, value = rname, placeholder = "auto")),
          column(1, del_btn)
        ))
    } else if (alchemy) {
      div(class = "resp-row", id = paste0("resp_row_", rid),
        fluidRow(
          column(5, textInput(paste0("resp_label_", rid), NULL, value = label, placeholder = "Response text")),
          column(3, textInput(paste0("resp_rname_", rid), NULL, value = rname, placeholder = "auto")),
          column(2, textInput(paste0("resp_value_", rid), NULL, value = value, placeholder = "int")),
          column(1, del_btn)
        ))
    } else {
      div(class = "resp-row", id = paste0("resp_row_", rid),
        fluidRow(
          column(6, textInput(paste0("resp_label_", rid), NULL, value = label, placeholder = "Response text")),
          column(4, textInput(paste0("resp_rname_", rid), NULL, value = rname, placeholder = "auto")),
          column(1, del_btn)
        ))
    }
  }

  make_scale_row <- function(rid, label = "", value = "") {
    alchemy <- is_alchemy(isolate(input$year) %||% 0)
    del_btn <- tags$button(class = "btn btn-xs btn-danger",
      onclick = sprintf("Shiny.setInputValue('delete_scale_row','%s',{priority:'event'})", rid), "×")
    if (alchemy) {
      div(class = "resp-row", id = paste0("scale_row_", rid),
        fluidRow(
          column(6, textInput(paste0("scale_label_", rid), NULL, value = label, placeholder = "e.g. Never")),
          column(3, textInput(paste0("scale_value_", rid), NULL, value = value, placeholder = "int")),
          column(1, del_btn)
        ))
    } else {
      div(class = "resp-row", id = paste0("scale_row_", rid),
        fluidRow(
          column(9, textInput(paste0("scale_label_", rid), NULL, value = label, placeholder = "e.g. Never")),
          column(1, del_btn)
        ))
    }
  }

  make_writein_row <- function(rid, label = "", type = "character", wname = "") {
    del_btn <- tags$button(class = "btn btn-xs btn-danger",
      onclick = sprintf("Shiny.setInputValue('delete_writein_row','%s',{priority:'event'})", rid), "×")
    div(class = "resp-row", id = paste0("writein_row_", rid),
      fluidRow(
        column(4, textInput(paste0("wi_label_", rid), NULL, value = label, placeholder = "e.g. Other (specify):")),
        column(3, selectInput(paste0("wi_type_",  rid), NULL, choices = c("character","numeric"), selected = type)),
        column(3, textInput(paste0("wi_wname_", rid), NULL, value = wname, placeholder = "e.g. otherText")),
        column(1, del_btn)
      ))
  }

  # ── Bulk paste toggles ─────────────────────────────────────────────────────
  observeEvent(input$toggle_bulk_resp,  shinyjs::toggle("bulk_resp_panel"))
  observeEvent(input$toggle_bulk_scale, shinyjs::toggle("bulk_scale_panel"))

  # ── Bulk import — responses ────────────────────────────────────────────────
  observeEvent(input$bulk_resp_import, {
    txt <- trimws(input$bulk_resp_text %||% "")
    if (nchar(txt) == 0) return()
    rows <- parse_bulk(txt)
    for (row in rows) {
      rid <- next_id()
      insertUI(selector = "#response_rows", where = "beforeEnd",
               ui = make_response_row(rid, row$label, row$rname, row$value), immediate = TRUE)
      rv$resp_row_ids <- c(rv$resp_row_ids, rid)
    }
    updateTextAreaInput(session, "bulk_resp_text", value = "")
    shinyjs::hide("bulk_resp_panel")
  })

  # ── Bulk import — scale ────────────────────────────────────────────────────
  observeEvent(input$bulk_scale_import, {
    txt <- trimws(input$bulk_scale_text %||% "")
    if (nchar(txt) == 0) return()
    rows <- parse_bulk(txt)
    for (row in rows) {
      rid <- next_id()
      insertUI(selector = "#scale_rows", where = "beforeEnd",
               ui = make_scale_row(rid, row$label, row$value), immediate = TRUE)
      rv$scale_row_ids <- c(rv$scale_row_ids, rid)
    }
    updateTextAreaInput(session, "bulk_scale_text", value = "")
    shinyjs::hide("bulk_scale_panel")
  })

  # ── Clear rows helper ──────────────────────────────────────────────────────
  clear_rows <- function() {
    for (rid in rv$resp_row_ids)    removeUI(paste0("#resp_row_",    rid), immediate = TRUE)
    for (rid in rv$scale_row_ids)   removeUI(paste0("#scale_row_",   rid), immediate = TRUE)
    for (rid in rv$writein_row_ids) removeUI(paste0("#writein_row_", rid), immediate = TRUE)
    rv$resp_row_ids    <- character(0)
    rv$scale_row_ids   <- character(0)
    rv$writein_row_ids <- character(0)
  }

  # ── Question type change ───────────────────────────────────────────────────
  observeEvent(input$questionType, {
    if (rv$suppress_type_change) return()
    clear_rows()
  }, ignoreInit = TRUE)

  # ── Add row buttons ────────────────────────────────────────────────────────
  observeEvent(input$add_response, {
    rid <- next_id()
    insertUI("#response_rows", "beforeEnd", make_response_row(rid), immediate = TRUE)
    rv$resp_row_ids <- c(rv$resp_row_ids, rid)
  })
  observeEvent(input$add_scale, {
    rid <- next_id()
    insertUI("#scale_rows", "beforeEnd", make_scale_row(rid), immediate = TRUE)
    rv$scale_row_ids <- c(rv$scale_row_ids, rid)
  })
  observeEvent(input$add_writein, {
    rid <- next_id()
    insertUI("#writein_rows", "beforeEnd", make_writein_row(rid), immediate = TRUE)
    rv$writein_row_ids <- c(rv$writein_row_ids, rid)
  })

  # ── Delete rows ────────────────────────────────────────────────────────────
  observeEvent(input$delete_resp_row, {
    rid <- input$delete_resp_row
    removeUI(paste0("#resp_row_", rid), immediate = TRUE)
    rv$resp_row_ids <- setdiff(rv$resp_row_ids, rid)
  })
  observeEvent(input$delete_scale_row, {
    rid <- input$delete_scale_row
    removeUI(paste0("#scale_row_", rid), immediate = TRUE)
    rv$scale_row_ids <- setdiff(rv$scale_row_ids, rid)
  })
  observeEvent(input$delete_writein_row, {
    rid <- input$delete_writein_row
    removeUI(paste0("#writein_row_", rid), immediate = TRUE)
    rv$writein_row_ids <- setdiff(rv$writein_row_ids, rid)
  })

  # ── Collect form state ─────────────────────────────────────────────────────
  collect_form <- reactive({
    qt          <- input$questionType %||% "selectOne"
    resp_labels <- sapply(rv$resp_row_ids, function(rid) input[[paste0("resp_label_", rid)]] %||% "")
    resp_rnames <- sapply(rv$resp_row_ids, function(rid) {
      v <- input[[paste0("resp_rname_", rid)]] %||% ""
      if (nchar(trimws(v)) == 0) to_rname(input[[paste0("resp_label_", rid)]] %||% "") else v
    })
    resp_values  <- sapply(rv$resp_row_ids,    function(rid) input[[paste0("resp_value_", rid)]] %||% "")
    scale_labels <- sapply(rv$scale_row_ids,   function(rid) input[[paste0("scale_label_", rid)]] %||% "")
    scale_values <- sapply(rv$scale_row_ids,   function(rid) input[[paste0("scale_value_", rid)]] %||% "")
    wi_labels    <- sapply(rv$writein_row_ids, function(rid) input[[paste0("wi_label_", rid)]] %||% "")
    wi_types     <- sapply(rv$writein_row_ids, function(rid) input[[paste0("wi_type_",  rid)]] %||% "character")
    wi_wnames    <- sapply(rv$writein_row_ids, function(rid) input[[paste0("wi_wname_", rid)]] %||% "")
    wname <- if (length(wi_wnames) > 0 && any(nchar(trimws(wi_wnames)) > 0))
      wi_wnames[nchar(trimws(wi_wnames)) > 0][1] else ""

    list(
      qname            = input$qname        %||% "",
      year             = input$year         %||% 2025,
      dname            = input$dname        %||% "",
      questionText     = input$questionText %||% "",
      questionType     = qt,
      text_pattern     = input$text_pattern %||% "",
      resp_labels      = resp_labels,
      rnames           = resp_rnames,
      resp_values      = resp_values,
      scale_row_labels = resp_labels,
      scale_opts       = scale_labels,
      scale_vals       = scale_values,
      wi_labels        = wi_labels,
      wi_types         = wi_types,
      wi_wnames        = wi_wnames,
      wname            = wname,
      notes_qname      = input$notes_qname %||% "",
      notes_year       = input$notes_year  %||% ""
    )
  })

  # ── Preview ────────────────────────────────────────────────────────────────
  output$preview <- renderText({
    d <- collect_form()
    if (nchar(trimws(d$qname)) == 0) return("# Fill in the form to see a preview...")
    tryCatch(build_man_call(d), error = function(e) paste("# Error:", e$message))
  })

  # ── Save ───────────────────────────────────────────────────────────────────
  observeEvent(input$save_record, {
    d <- collect_form()
    if (nchar(trimws(d$qname)) == 0) {
      showNotification("Please enter a name or question ID before saving.", type = "warning")
      return()
    }
    idx <- rv$editing_idx
    if (!is.null(idx)) {
      rv$records[[idx]] <- d
      rv$editing_idx <- NULL
      showNotification(paste0("Updated '", d$qname, "'."), type = "message")
    } else {
      rv$records <- c(rv$records, list(d))
      showNotification(paste0("Saved '", d$qname, "'."), type = "message")
    }
    do_clear_form()
  })

  # ── Clear form (year is intentionally NOT reset) ───────────────────────────
  do_clear_form <- function() {
    updateTextInput(session,      "qname",        value = "")
    # year intentionally kept
    updateTextInput(session,      "dname",         value = "")
    updateTextAreaInput(session,  "questionText",  value = "")
    updateSelectInput(session,    "questionType",  selected = "selectOne")
    updateTextInput(session,      "text_pattern",  value = "")
    updateTextAreaInput(session,  "notes_qname",   value = "")
    updateTextAreaInput(session,  "notes_year",    value = "")
    clear_rows()
    rv$editing_idx <- NULL
  }

  observeEvent(input$clear_form, do_clear_form())

  # ── Record list ────────────────────────────────────────────────────────────
  output$record_list <- renderUI({
    recs <- rv$records
    if (length(recs) == 0)
      return(div(style = "color:#6b7280; font-size:13px;", "No records saved yet."))
    tagList(lapply(seq_along(recs), function(i) {
      d <- recs[[i]]
      div(class = "record-card",
        fluidRow(
          column(8,
            tags$b(d$qname),
            tags$span(style = "color:#6b7280; font-size:12px; margin-left:8px;",
                      paste0(d$year, " · ", d$questionType))
          ),
          column(4, style = "text-align:right;",
            tags$button(class = "btn btn-xs btn-info", style = "margin-right:4px;",
              onclick = sprintf("Shiny.setInputValue('edit_record_idx',%d,{priority:'event'})", i),
              "Edit"),
            tags$button(class = "btn btn-xs btn-danger",
              onclick = sprintf("Shiny.setInputValue('delete_record_idx',%d,{priority:'event'})", i),
              "Delete")
          )
        )
      )
    }))
  })

  # ── Edit ───────────────────────────────────────────────────────────────────
  observeEvent(input$edit_record_idx, {
    i <- input$edit_record_idx
    d <- rv$records[[i]]
    rv$editing_idx       <- i
    rv$suppress_type_change <- TRUE
    clear_rows()
    updateTextInput(session,     "qname",        value = d$qname)
    updateNumericInput(session,  "year",          value = d$year)
    updateTextInput(session,     "dname",         value = d$dname %||% "")
    updateTextAreaInput(session, "questionText",  value = d$questionText)
    updateSelectInput(session,   "questionType",  selected = d$questionType)
    updateTextInput(session,     "text_pattern",  value = d$text_pattern %||% "")
    updateTextAreaInput(session, "notes_qname",   value = d$notes_qname %||% "")
    updateTextAreaInput(session, "notes_year",    value = d$notes_year  %||% "")
    qt <- d$questionType
    shinyjs::delay(200, {
      src_labels <- if (qt == "selectScale") d$scale_row_labels else d$resp_labels
      src_rnames <- d$rnames
      src_values <- if (qt == "selectScale") character(0) else d$resp_values
      for (j in seq_along(src_labels)) {
        rid     <- next_id()
        rname_j <- if (j <= length(src_rnames)) src_rnames[j] else ""
        value_j <- if (j <= length(src_values)) src_values[j] else ""
        insertUI("#response_rows", "beforeEnd",
                 make_response_row(rid, src_labels[j], rname_j, value_j), immediate = TRUE)
        rv$resp_row_ids <- c(rv$resp_row_ids, rid)
      }
      if (qt == "selectScale") {
        for (j in seq_along(d$scale_opts)) {
          rid   <- next_id()
          val_j <- if (j <= length(d$scale_vals)) d$scale_vals[j] else ""
          insertUI("#scale_rows", "beforeEnd",
                   make_scale_row(rid, d$scale_opts[j], val_j), immediate = TRUE)
          rv$scale_row_ids <- c(rv$scale_row_ids, rid)
        }
      }
      if (qt %in% c("selectOne", "selectMultiple")) {
        for (j in seq_along(d$wi_labels)) {
          rid     <- next_id()
          type_j  <- if (j <= length(d$wi_types))  d$wi_types[j]  else "character"
          wname_j <- if (!is.null(d$wi_wnames) && j <= length(d$wi_wnames)) d$wi_wnames[[j]] else ""
          insertUI("#writein_rows", "beforeEnd",
                   make_writein_row(rid, d$wi_labels[j], type_j, wname_j), immediate = TRUE)
          rv$writein_row_ids <- c(rv$writein_row_ids, rid)
        }
      }
      rv$suppress_type_change <- FALSE
    })
  })

  # ── Delete ─────────────────────────────────────────────────────────────────
  observeEvent(input$delete_record_idx, {
    i  <- input$delete_record_idx
    qn <- rv$records[[i]]$qname
    rv$records <- rv$records[-i]
    if (!is.null(rv$editing_idx) && rv$editing_idx == i) {
      rv$editing_idx <- NULL
      do_clear_form()
    }
    showNotification(paste0("Deleted '", qn, "'."), type = "warning")
  })

  # ── Download ───────────────────────────────────────────────────────────────
  output$download_r <- downloadHandler(
    filename = function() paste0("man_census_", Sys.Date(), ".R"),
    content  = function(file) {
      if (length(rv$records) == 0)
        writeLines("# No records saved.", file)
      else
        writeLines(generate_r_file(rv$records), file)
    }
  )
}

shinyApp(ui, server)
